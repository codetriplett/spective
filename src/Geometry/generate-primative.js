export function generatePrimative (x = 0, y = 0, z = 0) {
	return [
		`v ${x} 0 0`,
		`v ${x} 0 ${z}`,
		`v 0 0 ${z}`,
		'v 0 0 0',
		`v ${x} ${y} 0`,
		`v ${x} ${y} ${z}`,
		`v 0 ${y} ${z}`,
		`v 0 ${y} 0`,
		'vt 0 0',
		`vt 0 ${y}`,
		`vt ${x} 0`,
		`vt ${x} ${y}`,
		`vt ${z} 0`,
		`vt ${z} ${y}`,
		`vt 0 ${z}`,
		`vt ${x} ${z}`,
		'vn 0 -1 0',
		'vn 0 1 0',
		'vn 1 0 0',
		'vn 0 0 1',
		'vn -1 0 0',
		'vn 0 0 -1',
		'f 1/3/1 2/8/1 3/7/1 4/1/1',
		'f 5/8/2 8/7/2 7/1/2 6/3/2',
		'f 1/5/3 5/6/3 6/2/3 2/1/3',
		'f 2/3/4 6/4/4 7/2/4 3/1/4',
		'f 3/5/5 7/6/5 8/2/5 4/1/5',
		'f 5/2/6 1/1/6 4/3/6 8/4/6'
	].join('\n');
}
